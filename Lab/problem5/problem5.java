package Lab.problem5;
//  Write a java program to determine the addition, subtraction, multiplication and division of two numbers using inheritance.
public class problem5 {
    public void addition(int numberOne, int numberTwo){
        int additionSum = numberOne + numberTwo;
        System.out.println("Addition : " + additionSum);

    }
    
    public void subtraction(int numberOne, int numberTwo){
        int subtraction = numberOne - numberTwo;
        System.out.println("subtraction : " + subtraction);
    }

    public void multiplication(int numberOne, int numberTwo){
        int multiplication = numberOne * numberTwo;
        System.out.println("multiplication : " + multiplication);
    }

    public void division(int numberOne, int numberTwo){
        int division = numberOne / numberTwo;
        System.out.println("division : " + division);
    }
}





